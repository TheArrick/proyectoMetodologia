    private void cancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelarActionPerformed
        String usuario = nomUsuario.getText();
        char[] con = campoContras.getPassword();
        String contra = new String(con);
        bienvenido.setVisible(false);
        jLabel3.setVisible(false);
        if(usuario.equals("") || contra.equals("")){
           jLabel3.setVisible(true);
        }else{
            jLabel3.setVisible(false);
            Usuario user = gestorUsuarios.buscarUsuario(usuario, contra);
            if (user != null) {
                bienvenido.setVisible(true);
                menuUs = new MenuUsuario(user);
                menuUs.setVisible(true);
                this.dispose(); 
            } else {
                jLabel3.setVisible(true);
            }
        
        }
    }//GEN-LAST:event_cancelarActionPerformed

